class ForSum {

    public static void main(String args[]) {

        int i;

        int sum = 0;

        for(i = 0; i <= 5; ++i) {

            sum = sum + i;

        }
        
        System.out.println("The sum of the numbers from 0 to 5 is " + sum);

    }
}

