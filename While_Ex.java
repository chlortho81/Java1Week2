//Aaron Whittle While loop example

class While_Ex {

    public static void main(String args[]) {

        int i=0;

        while(i <= 5) {
            System.out.println("The value of i is " + i);
            i++;
        }
        
        i=5;

        while(i >= 0) {
            System.out.println("The value of i is " + i);
            i--;
        }
    }
}


        
