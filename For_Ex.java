// Aaron Whittle For loop example

class For_Ex {

    public static void main(String args[]) {

        int i;

        for(i=1; i<=5; i++) {
            System.out.println("The value of i is " + i);
        }
        for(i=5; i>=0; --i) {
            System.out.println("The value of i is " + i);
        }
    }
}



            
